if False:

    print('')
    print('-=' * 31 + '-')
    print('')

    print('Verdadeiro.')
    print('teste teste2')

    print('')
    print('-=' * 31 + '-')
    print('')

elif True:

    print('')
    print('-=' * 31 + '-')
    print('')

    print('Agora é verdadeiro.')
    nome = str(input('Qual o seu nome? '))

    print('Seu nome é {n}'.format(n=nome))

    print('')
    print('-=' * 31 + '-')
    print('')

elif False:

    print('')
    print('-=' * 31 + '-')
    print('')

    print('Mais uma verdadeira.')
    print(22 + 22)

    print('')
    print('-=' * 31 + '-')
    print('')

else:

    print('')
    print('-=' * 31 + '-')
    print('')

    print('Não é verdadeiro.')
    print('Olá')

    print('')
    print('-=' * 31 + '-')
    print('')
